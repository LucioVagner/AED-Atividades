/*Algoritmos e Estruturas de Dados I

Exercício 1: Em um sistema de comunicação, pacotes de dados chegam
inicialmente em uma pilha devido à forma como foram armazenados
temporariamente na memória. Entretanto, antes do envio pela rede, os pacotes
precisam ser transmitidos na ordem correta de chegada, utilizando uma fila.
Seu objetivo é implementar um programa em C que:
receba uma sequência de pacotes identificados por números inteiros;
armazene inicialmente os pacotes em uma pilha;
transfira os elementos da pilha para uma fila;
exiba a ordem final de transmissão dos pacotes.
Exemplo de execução:
Entrada: 5
10 20 30 40 50
Saída: 50 40 30 20 10*/